   document.addEventListener('DOMContentLoaded', () => {
    const tabs = document.querySelectorAll('.tab-button');
    const contents = document.querySelectorAll('.tab-content');
    const featureImage = document.querySelector('#feature-display');
    const featureContent = document.querySelector('#feature-content');

    tabs.forEach(tab => {
        tab.addEventListener('click', (e) => {
            e.preventDefault(); // Prevent default behavior to avoid anchor or focus scrolling
            e.stopPropagation(); // Stop event bubbling to prevent unintended triggers

            // Remove active class from all tabs and hide all contents
            tabs.forEach(t => t.classList.remove('active'));
            contents.forEach(c => c.classList.add('hidden'));

            // Add active class to clicked tab and show corresponding content
            tab.classList.add('active');
            const tabId = tab.getAttribute('data-tab');
            const content = document.getElementById(tabId);
            content.classList.remove('hidden');

            // Update image source and alt text
            featureImage.src = 'https://bizadmin.com.au/theme-assets/Landingpageassets/assets/hr/features.jpg';
            featureImage.alt = `${tabId.charAt(0).toUpperCase() + tabId.slice(1)} Feature`;

            // Smooth scroll to the feature content section
            featureContent.scrollIntoView({
                behavior: 'smooth',
                block: 'start',
                inline: 'nearest'
            });

            // Prevent focus-related scrolling by resetting focus
            tab.blur(); // Remove focus from the clicked button
        });
    });

    // Prevent page from jumping on load
    window.scrollTo(0, 0); // Ensure page starts at the top on load
});
</script>

