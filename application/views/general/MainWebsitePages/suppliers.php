document.addEventListener('DOMContentLoaded', () => {
    const tabs = document.querySelectorAll('.tab-button');
    const contents = document.querySelectorAll('.tab-content');
    const featureImage = document.querySelector('#feature-display');
    const featureContent = document.querySelector('#feature-content');

    tabs.forEach(tab => {
        tab.addEventListener('click', (e) => {
            e.preventDefault();
            e.stopPropagation();

            tabs.forEach(t => t.classList.remove('active'));
            contents.forEach(c => c.classList.add('hidden'));

            tab.classList.add('active');
            const tabId = tab.getAttribute('data-tab');
            const content = document.getElementById(tabId);
            content.classList.remove('hidden');

            featureImage.src = 'https://bizadmin.com.au/theme-assets/Landingpageassets/assets/suppliers/feature.jpg';
            featureImage.alt = `${tabId.charAt(0).toUpperCase() + tabId.slice(1).replace(/-/g, ' ')} Feature`;

            featureContent.scrollIntoView({
                behavior: 'smooth',
                block: 'start',
                inline: 'nearest'
            });

            tab.blur();
        });
    });

    window.scrollTo(0, 0);
});
</script>

